# 數字運算
x = 5
y = 5
# 加法
total = x + y
print(total)
# 減法
total = x - y
print(total)
# 乘法
total = x * y
print(total)
# 除法
total = x / y
print(total)
# 次方
total = x ** y
print(total)
# 整數除法 7/6 = 1
total = x // y
print(total)
# 取餘數
total = x % y
print(total)

# 字串運算
s="Hello"
print(s)
# 跳脫 字串中想加入符號
s="Hell\"o"
print(s)
# 換行
s="Hello\nworld" 
s="""Hello
world"""
print(s)
s="hello"*3+"world"
print(s)
s="Hello"
# 字串會對內容字元編號(索引),從0開始起
print(s[1:4])
