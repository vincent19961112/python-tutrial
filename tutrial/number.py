# 如何使用數字，數字的用法 (先乘除後加減)
# +加法
print(8+5)
# -減法
print(8-5)
# *乘法
print(8*5)
# /除法
print(8/2)
# 變數
base = 2
print(8+base)
# -減法
print(8-base)
# *乘法
print(8*base)
# /除法
print(8/base)

# 函式
number = -8
# 轉換成字串
print("印出轉成字串的數字"+str(number))
# 絕對值
print(abs(number))
# pow(x,y) x次方的基底，y幾次方
print(pow(number,2))
# max() 回傳最大的數字
print(max(1,2,50,100))
# min() 回傳最小的數字
print(min(1,2,50,100))
# round() 取四捨五入
print(round(4.6))
# str() 將數字轉成字串
print(str(54.2))

from math import *

# floor() 無條件捨去
print(floor(4.1))
# ceil() 無條件進位
print(ceil(4.1))
# sqrt() 開根號
print(sqrt(4))